public class Doctor {
	private String Dno = null;
	private String Dname = null;
	private String Dsex = null;
	private String Dbirth = null;
	private String Dphone = null;
	private String Demail = null;
	private String Dbrief = null;
	private String Dpwd = null;
	
	public String getDno() {
		return Dno;
	}
	public void setDno(String dno) {
		Dno = dno;
	}
	public String getDname() {
		return Dname;
	}
	public void setDname(String dname) {
		Dname = dname;
	}
	public String getDsex() {
		return Dsex;
	}
	public void setDsex(String dsex) {
		Dsex = dsex;
	}
	public String getDbirth() {
		return Dbirth;
	}
	public void setDbirth(String dbirth) {
		Dbirth = dbirth;
	}
	public String getDphone() {
		return Dphone;
	}
	public void setDphone(String dphone) {
		Dphone = dphone;
	}
	public String getDemail() {
		return Demail;
	}
	public void setDemail(String demail) {
		Demail = demail;
	}
	public String getDbrief() {
		return Dbrief;
	}
	public void setDbrief(String dbrief) {
		Dbrief = dbrief;
	}
	public String getDpwd() {
		return Dpwd;
	}
	public void setDpwd(String dpwd) {
		Dpwd = dpwd;
	}
}
